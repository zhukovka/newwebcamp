            <li><strong> Twitter Bootstrap</strong> - библиотека готовых решений для верстальщика;</li>
            <li><strong> Landing Page design</strong> - разработка "одностраничного" целевого сайта для конкретного товара/услуги;</li>
            <li><strong> Responsive design</strong> - адаптивная верстка под различные размеры экрана; </li>
            <li><strong> Valid development</strong> - валидная верстка согласно стандартам W3C;</li>
            <li><strong> Кроссбраузерная верстка</strong> - как переверстать сайт в стиле 90-х чтобы его обработал <b>IE6</b>;</li>
