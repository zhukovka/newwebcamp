        <h3 class="knowledge_header">Мы обучаем:</h3>
        <h4>Разметка и верстка страницы:</h4>
        <ul>
            <li><strong> HTML/HTML5</strong> - обучение с нуля;</li>
            <li><strong> CSS/CSS3</strong> - обучение с нуля;</li>
